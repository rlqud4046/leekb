package com.sist;

// 패키지 : 동일한 클래스들의 모임 ( 폴더와 유사 )
// 			소문자로 작성하는것이 관례

/*
 * java 프로그램 특징
 * 1. 객체지향 언어이다
 * 2. 클래스 단위로 프로그램을 작성한다
 * 3. 한 문장은 세미콜론으로 끝난다
 * 4. 클래스와 이름은 대문자로 시작한다 
 *    메서드는 소문자로 작성한다
 * 5. main() : 프로그램의 시작 위치를 의미한다
 */

public class Ex01 { // 클래스를 만들 때 대문자로 시작

	public static void main(String[] args) {
	
		// main 입력 후 ctrl + spacebar (자동완성) 
		
		System.out.println("안녕하세요~ 자바에 오신것을 환영합니다.");
		System.out.println("안녕하세요!!");
		System.out.print("방가방가~~~");
		System.out.print("반갑습니다.");
		System.out.println();
		System.out.println();
		System.out.println("저는 이기병입니다.");
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println();
		
		/*
		 * System.out : 표준 출력장치 (모니터. 콘솔창)
		 * println() : 지정된 장치에 결과물을 출력하는 메서드
		 * 		ln : line의 약자로 출력 후 줄을 바꿔줌
		 * 
		 * print() : 다음줄로 이동하지 않고 오른쪽에 이어서 결과물을 출력
		 * 
		 * 자바 프로그램은 JVM이 제일 먼저  main() 메서드를 찾아서 
		 * 메서드 안에 있는 문장을 위에서 아래로 차례대로 실행한다.
		 * 
		 * 
		 * 
		 * 기본적인 단축키   
		 * 	실행 단축키  ctrl f11
		 * 	축약어 단축키 ctrl + spacebar  ex) sysout , main
		 * 
		 * 	
		 */
	}

}
